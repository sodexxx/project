#include "../include/lab3.h"

// Главная функция запуска лабораторной работы 3
void solve_lab3() {
    printf("\n=== Лабораторная работа 3: Подсчёт слов с одинаковыми первыми буквами ===\n");
    printf("Введите текст (завершите ввод Ctrl+D на Linux/Mac или Ctrl+Z на Windows):\n");

    int c;          // текущий символ из потока
    int prev_c;     // предыдущий символ
    int p_p_c;      // символ перед предыдущим
    int cnt = 0;    // счётчик слов

    // Инициализация
    prev_c = ' ';
    p_p_c = ' ';

    // Цикл чтения символов из потока
    while ((c = getchar()) != EOF) {
        // Проверяем, был ли перед текущим символом разделитель
        if (p_p_c == ' ' || p_p_c == '.' || p_p_c == '\n' || p_p_c == ',') {
            // Нашли начало нового слова — проверяем первые две буквы
            if (prev_c == c && c != ' ' && c != '.' && c != '\n' && c != ',') {
                cnt++;
            }
        }

        // Сдвигаем "окно" символов
        p_p_c = prev_c;
        prev_c = c;
    }

    // Вывод результата
    printf("Количество слов с одинаковыми первыми буквами: %d\n", cnt);
}