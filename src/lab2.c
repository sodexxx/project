#include "../include/lab2.h"

// Главная функция запуска лабораторной работы 2
void solve_lab2() {
    printf("\n=== Лабораторная работа 2: Сумма ряда ===\n");

    float S;      // сумма ряда
    int i;        // счётчик цикла
    int N;        // число элементов ряда
    int k;        // знаменатель, номер элемента последовательности
    float a;      // член последовательности

    // Ввод данных
    printf("Введите количество членов ряда (N): ");
    scanf("%d", &N);

    // Инициализация
    S = 0;
    i = 0;
    k = 1;

    // Цикл вычисления суммы
    while (i < N) {
        a = (float)(i + 1) / ((1 + k) * (1 + k));  // приведение к float для точности
        S = S + a;
        i = i + 1;
        k = k + 1;
    }

    // Вывод результата
    printf("Сумма ряда равна: %.6f\n", S);
}